export function getCreaturesByNd(nd: number): Creature[] {
  return CREATURES.filter(c => c.nd === nd)
}

export function getCreaturesByType(type: CreatureType): Creature[] {
  return CREATURES.filter(c => c.type === type)
}

export function searchCreatures(query: string): Creature[] {
  const q = query.toLowerCase()
  return CREATURES.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.type.toLowerCase().includes(q)
  )
}

